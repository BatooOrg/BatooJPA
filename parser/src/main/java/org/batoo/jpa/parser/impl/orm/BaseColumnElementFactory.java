package org.batoo.jpa.parser.impl.orm;

import java.util.Map;

import org.batoo.jpa.parser.metadata.BaseColumnMetadata;

/**
 * 
 * @author hceylan
 * @since $version
 */
public abstract class BaseColumnElementFactory extends ChildElementFactory implements BaseColumnMetadata {

	private String name;
	private String columnDefinition;
	private boolean insertable;
	private boolean nullable;
	private String table;
	private boolean unique;
	private boolean updatable;

	/**
	 * @param parent
	 *            the metamodel
	 * @param attributes
	 *            the attributes
	 * 
	 * @since $version
	 * @author hceylan
	 */
	public BaseColumnElementFactory(ParentElementFactory parent, Map<String, String> attributes) {
		super(parent, attributes);
	}

	/**
	 * {@inheritDoc}
	 * 
	 */
	@Override
	protected void generate() {
		this.name = this.getAttribute(ATTR_NAME, EMPTY);
		this.columnDefinition = this.getAttribute(ATTR_COLUMN_DEFINITION, EMPTY);
		this.insertable = this.getAttribute(ATTR_INSERTABLE, false);
		this.nullable = this.getAttribute(ATTR_NULLABLE, false);
		this.table = this.getAttribute(ATTR_TABLE, EMPTY);
		this.unique = this.getAttribute(ATTR_UNIQUE, false);
		this.updatable = this.getAttribute(ATTR_UPDATABLE, true);
	}

	/**
	 * Returns the columnDefinition of the ColumnElementFactory.
	 * 
	 * @return the columnDefinition of the ColumnElementFactory
	 * 
	 * @since $version
	 * @author hceylan
	 */
	@Override
	public String getColumnDefinition() {
		return this.columnDefinition;
	}

	/**
	 * {@inheritDoc}
	 * 
	 */
	@Override
	public String getName() {
		return this.name;
	}

	/**
	 * {@inheritDoc}
	 * 
	 */
	@Override
	public String getTable() {
		return this.table;
	}

	/**
	 * {@inheritDoc}
	 * 
	 */
	@Override
	public boolean isInsertable() {
		return this.insertable;
	}

	/**
	 * {@inheritDoc}
	 * 
	 */
	@Override
	public boolean isNullable() {
		return this.nullable;
	}

	/**
	 * {@inheritDoc}
	 * 
	 */
	@Override
	public boolean isUnique() {
		return this.unique;
	}

	/**
	 * {@inheritDoc}
	 * 
	 */
	@Override
	public boolean isUpdatable() {
		return this.updatable;
	}

}
